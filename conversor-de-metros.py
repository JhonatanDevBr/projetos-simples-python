medida = float(input("Digite o valor em metros que se deseja transformar"))
cm = medida * 100
mm = medida * 1000

print("===========Conversor de Metros=============")
print("Valor digitado em metros: {}m.".format(medida))
print("Valor digitado em centímetros: {}cm.".format(cm))
print("Valor digitado em milímetros: {}mm.".format(mm))
